//************************************************************************
// void InitDisplay(void)
//************************************************************************
void glcd_Init(unsigned char mode)
{
	unsigned char i, j, k;

	// Reset the display
	PORTD = 0x00;
	GLCD_RST = 0;
	Delay10TCYx(15);
	GLCD_RST = 1;

	GLCD_E=0;
	GLCD_CS1=0;
	GLCD_CS2=0;
	GLCD_RS=0;
	Delay1TCY();        			//dlai pour initialisation

 	glcd_WriteByte(GLCD_LEFT,  0xC0);    // Specifie la premire ligne RAM line en haut
  	glcd_WriteByte(GLCD_RIGHT, 0xC0);    //   de l'cran
   	glcd_WriteByte(GLCD_LEFT,  0x40);    // Place l'adresse de colonne  0
   	glcd_WriteByte(GLCD_RIGHT, 0x40);
   	glcd_WriteByte(GLCD_LEFT,  0xB8);    // Place l'adresse de la page  0
   	glcd_WriteByte(GLCD_RIGHT, 0xB8);

	if(mode == GLCD_ON)
  {
      glcd_WriteByte(GLCD_LEFT,  0x3F); // Allume le GLCD
      glcd_WriteByte(GLCD_RIGHT, 0x3F);
  }
   else
  {
      glcd_WriteByte(GLCD_LEFT,  0x3E); // Eteint le GLCD
      glcd_WriteByte(GLCD_RIGHT, 0x3E);
   }

	glcd_FillScreen(0);                // CLearScreen !

}

//************************************************************************
// void WriteByte(unsigned char byt)
//************************************************************************
void glcd_WriteByte(unsigned char side, unsigned char data)
{
	if(side)
	{                   // Slection du cot pour l'criture
     	GLCD_CS1 = 1;
		GLCD_CS2 = 0;
	}
   	else
	{
		GLCD_CS2 = 0;
      	GLCD_CS2 = 1;
	}

	GLCD_DATA_TRIS=0;			//Port D en sortie
	GLCD_RW=0;					// Positionnement du bit pour criture

	WR_DATA = data;				// Placement des donnes sur le registre du GLCD

	// Delay10TCYx (10); 			//dlai 8,33ms

	GLCD_E = 1;					//Passage de E de 0  1
	Delay1TCY(); 				//dlai 833ns revu a 500ns
	GLCD_E = 0;					//Passage de E de 1  0
	Delay1TCY(); 				//dlai 833ns revu a 500ns
								// Le GLCD rcupre les donnes !

	GLCD_CS1 = 0;
	GLCD_CS2 = 0;				// relachement des ChipSelect
}


//************************************************************************
// void WriteByte(unsigned char byt)
//************************************************************************
unsigned char glcd_ReadByte(unsigned char side)
{
unsigned char data;

	if(side)
	{                   // Slection du cot pour l'criture
     	GLCD_CS1 = 1;
		GLCD_CS2 = 0;
	}
   	else
	{
		GLCD_CS2 = 0;
      	GLCD_CS2 = 1;
	}

	GLCD_DATA_TRIS = 0xFF;	//Port D en entre
	GLCD_RW = 1;			// Positionnement du bit pour lecture

	// Delay10TCYx (10); 		//dlai 8,33ms

	GLCD_E = 1;				//Passage de E de 0  1
	Delay1TCY(); 				//dlai 833ns revu a 500ns

	data = RD_DATA;			// Rcupration des donnes
	GLCD_E = 0;				//Passage de E de 1  0
	Delay1TCY(); 				//dlai 833ns revu a 500ns

	GLCD_CS1 = 0;
	GLCD_CS2 = 0;				// relachement des ChipSelect

	return data;
}

//************************************************************************
// void glcd_PlotPixel(unsigned char xpos, unsigned char ypos, unsigned char color)
//************************************************************************
void glcd_PlotPixel(unsigned char xpos, unsigned char ypos, unsigned char color)
{
   	unsigned char data;
   	unsigned char side = GLCD_LEFT;  			// On initialise sur le cot gauche

   	if(xpos > 63)             						// Si ce n'est pas le ct gauche, c'est le droit
   	{
      	xpos -= 64;
      	side = GLCD_RIGHT;
   	}
   	GLCD_RS=0;                        			 // Positionnement du bit pour instruction (adressage)

	xpos &=0b01111111;								// force  0 le MSB
	xpos |=0b01000000;								// Positionne  1 le bit 6.
	glcd_WriteByte(side, (xpos));                  	// Envoi la position horizontale
	glcd_WriteByte(side, (((ypos/8)) & 0xBF) | 0xB8);   // Envoi la position verticale (page)

	GLCD_RS=1;                        				// Positionnement du bit pour rcupration de donnes

	glcd_ReadByte(side);                         	// On doit lire 2 fois pour obtenir les donnes
	data = glcd_ReadByte(side);                  	//   l'adresse intialise

	GLCD_RS=0;                        			 	// Positionnement du bit pour instruction (adressage)

	glcd_WriteByte(side, xpos);                 	// reinitialise la position horizontale, incrementer lors de la lecture

	if(color)
		data=(data)|(1<<(ypos%8));					//Allume le pixel sans modifier les autres
	else
		data =(data)&(~(1<<(ypos%8)));				//Eteint le pixel sans modifier les autres

	GLCD_RS=1;  			       					// Positionnement du bit pour envoi de donnes

	glcd_WriteByte(side, data);   					// On crit les donnes
}

//************************************************************************
// void WriteChar(unsigned char ch)
//************************************************************************
void glcd_FillScreen(unsigned char color)
{
    unsigned char i, j;

    for(i = 0; i < 8; i ++)							// Boucle sur les pages verticales
    {
            GLCD_RS=0; 									// Positionnement du bit pour instruction (adressage)
        glcd_WriteByte(GLCD_LEFT, 0b01000000);      // Envoi la position horizontale  0
        glcd_WriteByte(GLCD_RIGHT, 0b01000000);
        glcd_WriteByte(GLCD_LEFT, i | 0b10111000);  // Envoi la position verticale (page 0)
        glcd_WriteByte(GLCD_RIGHT, i | 0b10111000);
        GLCD_RS=1;                                  // Positionnement du bit pour transfert de donnes

        for(j = 0; j < 64; j++)					// Boucle sur les colonnes
        {
            glcd_WriteByte(GLCD_LEFT, 0xFF*color);  // Allume ou teint les pixels
            glcd_WriteByte(GLCD_RIGHT, 0xFF*color);
        }
    }
}

//************************************************************************
// void glcd_SetCursor(unsigned char xpos,unsigned char ypos)
//************************************************************************
void glcd_SetCursor(unsigned char xpos,unsigned char ypos)
{
	unsigned char side = GLCD_LEFT;  			// On initialise sur le cot gauche

	if(xpos > 127 | ypos > 7)					// Si les coordonnes sont Hors limite, on ne traite pas
		return;

	x = xpos;									// Sauvegarde des donnes position absolue
	y = ypos;

   	if(xpos > 63)             					// Si ce n'est pas le ct gauche, c'est le droit
   	{
      xpos -= 64;								// xpos est la position relative au controleur G ou D
      side = GLCD_RIGHT;
   	}

	GLCD_RS=0; 									//Placement du bit pour envoi instruction (adressage)
		glcd_WriteByte(side, 0x40 | xpos);		//Envoi de l'adresse horizontale
		glcd_WriteByte(side, 0xB8 | ypos);		//Envoi de l'adresse verticale
	GLCD_RS=1; 									//Placement du bit pour transfert de donne
}

//************************************************************************
// void WriteChar8X8(unsigned char ch)
//************************************************************************
void glcd_WriteChar8X8(unsigned char ch, unsigned char color)
{
	unsigned char i,xpos;
	unsigned char side = GLCD_LEFT;  	// On initialise sur le cot gauche
	unsigned int chr;

	if (ch=='\n')						// dtection CR
	{
		x=0;
		y++;
		return;
	}

	if(x > 63)           			// Si ce n'est pas le ct gauche, c'est le droit
   	   	{
		side = GLCD_RIGHT;
		xpos=x-64;					// x dsigne la position absolue,
		}							//xpos est la position / au controleur doit ou gauche
	else
   		xpos=x;						//Lorsque l'on est  gauche x=xpos

	chr = (int)ch*8;				//Positionnement de chr au dbut du caractre (8 bytes par caractre)

	for(i = 0; i < 8; i++)			//on balaye les 8 colonnes du caractre
	{
	// if((xpos+i) > 63)             	// Si le caractre se trouve entre les deux moitis, on traite !
	if(xpos > 63)
		{
      	xpos -= 64;
      	side = GLCD_RIGHT;
	  	GLCD_RS=0; 					// Positionnement du bit pour instruction (adressage)
	  	glcd_WriteByte(side, 0x40 | xpos);			//on repositionne le curseur du GLCD
	  	glcd_WriteByte(side, 0xB8 | y);					// sur le ct droit
	  	GLCD_RS=1; 					// Positionnement du bit pour transfert de donnes
   		}
	if(color)
		glcd_WriteByte(side,Font8x8[chr+i]);	// on crit le caractre
	else
		glcd_WriteByte(side,~(Font8x8[chr+i]));
	xpos++;
	}
	x+=8;
}


